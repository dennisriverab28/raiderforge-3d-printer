//This updates the menu
int8_t menu_update_from_encoder(TIM_HandleTypeDef *htim)
{
    int16_t current_cnt = (int16_t)__HAL_TIM_GET_COUNTER(htim);
    int16_t diff = current_cnt - last_cnt;
    const int16_t base_count = 2;
    int8_t encoder_moved = 0;

    if (diff >= base_count) {
        menu_sel++;

        if (menu_sel >= MENU_COUNT) menu_sel = 0;
        last_cnt = current_cnt;   // lock to current count after step
        encoder_moved = 1;
    }
    else if (diff <= -base_count) {
        menu_sel--;
        if (menu_sel < 0) menu_sel = MENU_COUNT - 1;
        last_cnt = current_cnt;
        encoder_moved = 1;
    }

    return encoder_moved;
}


