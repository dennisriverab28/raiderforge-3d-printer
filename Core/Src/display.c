	u8g2_DrawXBM(&u8g2, 15, 23, 8, 5, image_Layer_6_bits);
    u8g2_DrawStr(&u8g2, 26, 19, ext_buf);   /* live hotend actual/target  */
    u8g2_DrawStr(&u8g2, 26, 29, bed_buf);   /* live bed    actual/target  */
	u8g2_DrawXBM(&u8g2, 2, 49, 14, 16, image_micro_sd_bits);
	u8g2_DrawLine(&u8g2, 0, 46, 127, 46);
