uint8_t u8x8_byte_stm32_hw_spi(u8x8_t *u8x8, uint8_t msg, uint8_t arg_int, void *arg_ptr)
{
    switch (msg)
    {
        case U8X8_MSG_BYTE_INIT:
            return 1;

        case U8X8_MSG_BYTE_SET_DC:
            return 1;

        case U8X8_MSG_BYTE_START_TRANSFER:
            HAL_GPIO_WritePin(Screen_CS_GPIO_Port, Screen_CS_Pin, GPIO_PIN_SET);
            return 1;

        case U8X8_MSG_BYTE_SEND:
            HAL_SPI_Transmit(&hspi3, (uint8_t*)arg_ptr, arg_int, HAL_MAX_DELAY);
            return 1;

        case U8X8_MSG_BYTE_END_TRANSFER:
            HAL_GPIO_WritePin(Screen_CS_GPIO_Port, Screen_CS_Pin, GPIO_PIN_RESET);
            return 1;

        default:
            return 0;
    }
}


uint8_t u8x8_gpio_and_delay_stm32(u8x8_t *u8x8, uint8_t msg, uint8_t arg_int, void *arg_ptr)
{
    switch (msg)
    {
    case U8X8_MSG_DELAY_MILLI:
        HAL_Delay(arg_int);
        return 1;
    case U8X8_MSG_DELAY_10MICRO:
        for (volatile uint32_t i = 0; i < (SystemCoreClock/1000000/3)*10*arg_int; i++) { __NOP(); }
        return 1;

    case U8X8_MSG_GPIO_RESET:
        // If you wired RST, toggle it here. If not, just return 1.
        // HAL_GPIO_WritePin(Screen_RST_GPIO_Port, Screen_RST_Pin, arg_int ? GPIO_PIN_SET : GPIO_PIN_RESET);
        return 1;
    }
    return 1;
}

